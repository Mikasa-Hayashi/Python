# match-case — это конструкция Python (с 3.10) для структурного сопоставления.
# 
# _ — универсальный шаблон, аналог else.
# 
# Хорошо подходит, когда нужно обработать одно значение с множеством 
#   вариантов или разные типы данных.
# 
# Не стоит использовать для двух-трёх простых условий — обычный if-elif-else 
#   будет достаточно.
# 
# Можно использовать для работы со структурами данных
# 
# Использование case <тип>() делает код коротким, читаемым и наглядным 
#   при работе с данными разных типов.

