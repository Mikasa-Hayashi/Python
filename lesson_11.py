# Конструкция match-case — это механизм структурного сопоставления (pattern matching), 
# который позволяет сравнить одно значение с несколькими шаблонами и выполнить 
# соответствующий блок кода.

# Синтаксис:
# match value:
#     case pattern1:
#         действие
#     case pattern2:
#         действие
#     case _:
#         действие по умолчанию

# match – это ключевое слово, обозначающее, что мы намереваемся 
# проверить значение переменной value.

# case — отдельная ветка обработки внутри match, которая выполняется 
# при совпадении шаблона. Это как отдельный if или elif.

# _ — специальный шаблон, который означает «любой другой случай». Аналог else.
# _ — аналог default в других языках программирования

